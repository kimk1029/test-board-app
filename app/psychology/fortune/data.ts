/** 시드 문자열로 0 ~ max-1 사이의 결정론적 인덱스 반환 */
export function getSeedIndex(seed: string, max: number): number {
  let hash = 0
  for (let i = 0; i < seed.length; i++) {
    hash = (hash * 31 + seed.charCodeAt(i)) >>> 0
  }
  return Math.abs(hash) % max
}
